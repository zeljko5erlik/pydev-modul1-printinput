x = input('Upisite prvi broj: ')
y = input('Upisite drugi broj: ')

x = int(x)
y = int(y)
zbroj = x + y
razlika = x - y
umnozak = x * y
kvocjent = x / y
potencija = x ** y
modulo = x % y

print(x, '+', y, '=', zbroj)
print(x, '-', y, '=', razlika)
print(x, '*', y, '=', umnozak)
print(x, '/', y, '=', kvocjent)
print(x, '**', y, '=', potencija)
print(x, '%', y, '=', modulo)